            this.results.profile_type = this.lang === 'ru' ? "–í –ø–æ–∏—Å–∫–µ –æ—Ä–∏–µ–Ω—Ç–∏—Ä–æ–≤" : "–ë–∞“ì—ã—Ç —ñ–∑–¥–µ—É “Ø—Å—Ç—ñ–Ω–¥–µ";
