    renderRadar(res) {
        const t = TRANSLATIONS[this.lang];
        const labels = Object.keys(res.block_scores).map(id => t.blockNames[id]);
        const data = Object.values(res.block_scores).map(d => d.norm);

        if (this.chart) this.chart.destroy();
        
        this.chart = new Chart(this.ui.radarCanvas, {
            type: 'radar',
            data: {
                labels: labels,
                datasets: [{
                    label: t.resultTitle,
                    data: data,
                    backgroundColor: 'rgba(37, 99, 235, 0.2)',
                    borderColor: '#2563eb',
                    pointBackgroundColor: '#2563eb',
                    pointBorderColor: '#fff',
                    pointHoverBackgroundColor: '#fff',
                    pointHoverBorderColor: '#2563eb'
                }]
            },
            options: {
                scales: {
                    r: {
                        beginAtZero: true,
                        max: 100,
                        ticks: { stepSize: 20, display: false },
                        grid: { color: '#e2e8f0' },
                        angleLines: { color: '#e2e8f0' },
                        pointLabels: {
                            font: { size: 12, weight: '600' },
                            color: '#1e293b'
                        }
                    }
                },
                plugins: {
                    legend: { display: false }
                },
                maintainAspectRatio: false
            }
        });
    },

    download() {
        const el = document.getElementById('report-content');
        const opt = {
            margin: 0, 
            filename: `V-Pro_Report_${this.lang.toUpperCase()}.pdf`,
            image: { type: 'jpeg', quality: 0.98 },
            html2canvas: { 
                scale: 2, 
                useCORS: true, 
                logging: false,
                letterRendering: true
            },
            jsPDF: { unit: 'mm', format: 'a4', orientation: 'portrait' },
            pagebreak: { mode: ['css', 'legacy'] }
        };

        html2pdf().set(opt).from(el).save();
    }
